const DIFFICULTY_WEIGHTS = {
  Easy: 0.2,
  Medium: 0.7,
  Hard: 0.1
};

const MAX_HISTORY = 25;
const STORAGE_KEY = "lcRandomizerHistory";

const resultsSection = document.getElementById("resultsSection");
const resultsContainer = document.getElementById("resultsContainer");
const historyList = document.getElementById("historyList");
const generateButton = document.getElementById("generateButton");
const refreshButton = document.getElementById("refreshButton");

function getRecentUrls() {
  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored ? JSON.parse(stored) : [];
}

function saveRecentUrls(urls) {
  const recent = getRecentUrls().concat(urls).slice(-MAX_HISTORY);
  window.localStorage.setItem(STORAGE_KEY, JSON.stringify(recent));
}

function renderHistory() {
  const history = getRecentUrls();
  historyList.innerHTML = "";
  if (!history.length) {
    const emptyItem = document.createElement("li");
    emptyItem.textContent = "No recent selections yet.";
    historyList.appendChild(emptyItem);
    return;
  }
  history.slice().reverse().forEach((url) => {
    const item = document.createElement("li");
    const link = document.createElement("a");
    link.href = url;
    link.target = "_blank";
    link.rel = "noopener noreferrer";
    link.textContent = url;
    item.appendChild(link);
    historyList.appendChild(item);
  });
}

function shuffle(array) {
  for (let i = array.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}

function randomByWeights(items, weights) {
  const total = items.reduce((sum, item) => sum + weights[item.difficulty], 0);
  let target = Math.random() * total;
  for (const item of items) {
    target -= weights[item.difficulty];
    if (target <= 0) {
      return item;
    }
  }
  return items[items.length - 1];
}

function getTopicGroups() {
  return PROBLEM_DATA.reduce((groups, item) => {
    if (!groups[item.topic]) groups[item.topic] = [];
    groups[item.topic].push(item);
    return groups;
  }, {});
}

function pickProblems() {
  const recentUrls = getRecentUrls();
  const topicGroups = getTopicGroups();
  const topics = Object.keys(topicGroups);
  const chosen = [];
  const usedTopics = new Set();
  const weightedData = PROBLEM_DATA.map((item) => ({
    ...item,
    repeatPenalty: recentUrls.includes(item.url) ? 0.2 : 1
  }));

  const difficultyTargets = ["Medium", "Medium", "Medium", "Easy"];
  if (Math.random() < 0.15) {
    difficultyTargets[3] = "Hard";
  }

  shuffle(difficultyTargets);

  difficultyTargets.forEach((difficulty) => {
    const availableTopics = topics.filter((topic) => !usedTopics.has(topic));
    let candidates = weightedData.filter((item) => item.difficulty === difficulty && !chosen.includes(item));
    if (availableTopics.length > 0) {
      const topicChoice = availableTopics[Math.floor(Math.random() * availableTopics.length)];
      const topicCandidates = candidates.filter((item) => item.topic === topicChoice);
      if (topicCandidates.length > 0) {
        candidates = topicCandidates;
      }
    }
    if (!candidates.length) {
      candidates = weightedData.filter((item) => !chosen.includes(item));
    }
    const picked = candidates.sort((a, b) => b.repeatPenalty - a.repeatPenalty)[Math.floor(Math.random() * candidates.length)];
    chosen.push(picked);
    usedTopics.add(picked.topic);
  });

  return chosen;
}

function showSelection(selection) {
  resultsContainer.innerHTML = "";
  selection.forEach((problem) => {
    const card = document.createElement("article");
    card.className = "problem-card";
    card.innerHTML = `
      <div>
        <h3><a href="${problem.url}" target="_blank" rel="noopener noreferrer">${problem.title}</a></h3>
        <div class="tags">
          <span class="tag topic">${problem.topic}</span>
          <span class="tag difficulty ${problem.difficulty.toLowerCase()}">${problem.difficulty}</span>
        </div>
      </div>
    `;
    resultsContainer.appendChild(card);
  });
  resultsSection.classList.remove("hidden");
}

function generateProblems() {
  const selection = pickProblems();
  showSelection(selection);
  saveRecentUrls(selection.map((item) => item.url));
  renderHistory();
}

generateButton.addEventListener("click", generateProblems);
refreshButton.addEventListener("click", generateProblems);

renderHistory();
